      case 'Comédia Romântica':
        return romanticComedyMainCourse;
      case 'Drama Romântico':
        return romanticDramaMainCourse;
      case 'Musical Romântico':
        return musicalRomanceMainCourse;
      case 'Romance Aventureiro':
        return adventureRomanceMainCourse;
      case 'Suspense Romântico':
        return thrillerRomanceMainCourse;
      default:
        return classicRomanceMainCourse;
    }
  }

  /// Obter 5 sobremesas para um tipo de Date Night
  static List<Recipe> getDessertsForDateType(String dateType) {
